package crypto

// Версия протокола MIRAGE (см. дизайн 02-architecture §5).
const (
	VersionMajor = 0
	VersionMinor = 2 // 0.2: Elligator2-эфемералы + MAC1 в рукопожатии (несовместимо с 0.1)
)

// Prologue возвращает Noise-prologue — байты, криптографически связывающие
// согласованные параметры с рукопожатием (защита от downgrade, 02 §5.2).
// На текущем этапе это версия и имя набора; позже сюда добавятся список
// cipher-suite и id профиля обфускации.
//
// ВАЖНО: prologue должен побайтно совпадать у клиента и сервера, иначе
// рукопожатие провалится (что и является защитой от подмены параметров).
func Prologue() []byte {
	return []byte{
		'M', 'I', 'R', 'A', 'G', 'E',
		VersionMajor,
		VersionMinor,
	}
}
